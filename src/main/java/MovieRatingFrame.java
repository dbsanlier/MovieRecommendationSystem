    private void btnGetRecsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGetRecsActionPerformed
        
        // Kullanıcı vektörü 
        Map<String, Double> vec = new LinkedHashMap<>();
        for (String id : dataLoader.getMovieColumnIds()) vec.put(id, 0.0);

        Set<String> selected = new LinkedHashSet<>();

        for (int i = 0; i < ROWS; i++) {
            String title = (String) cmbMovies[i].getSelectedItem();
            if (title == null) { warn("Line" + (i+1) + ": no  movie selected"); return; }
            if (selected.contains(title)) {
                warn("Line " + (i+1) + ": the same movie has already been selected, choose a different one.");
                return;
            }
            int rating;
            try {
                rating = Integer.parseInt(txtRatings[i].getText().trim());
                if (rating < 1 || rating > 5) throw new NumberFormatException();
            } catch (NumberFormatException ex) {
                warn("Line " + (i+1) + ": the score must be an integer between 1 and 5.");
                return;
            }
            selected.add(title);
            String id = moviePool.get(title);
            if (id != null) vec.put(id, (double) rating);
        }

        int X, K;
        try {
            X = Integer.parseInt(txtX.getText().trim());
            K = Integer.parseInt(txtK.getText().trim());
            if (X <= 0 || K <= 0) throw new NumberFormatException();
        } catch (NumberFormatException ex) {
            warn("X and K must be positive integers.");
            return;
        }

        txtResults.setText("Calculating...");
        btnGetRecs.setEnabled(false);
        txtResults.setText("Wait a moment...");

        final Map<String, Double> fVec = vec;
        final int fX = X, fK = K;
        new SwingWorker<List<String>, Void>() {
            @Override protected List<String> doInBackground() {
                return engine.recommendByRatings(fVec, fX, fK);
            }
            @Override protected void done() {
                try { showResults(fX, fK, get()); }
                catch (InterruptedException | ExecutionException ex) { txtResults.setText("Error: " + ex.getMessage()); }
            }
        }.execute();
        btnGetRecs.setEnabled(true);

    }//GEN-LAST:event_btnGetRecsActionPerformed

