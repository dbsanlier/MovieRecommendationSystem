    // Tüm filmlerden 10 tanesini rastgele seçim
    public void loadRandomMovies() {
        moviePool.clear();

        List<Map.Entry<String, String>> entries =
                new ArrayList<>(dataLoader.getMovieNames().entrySet()); // id → title
        Collections.shuffle(entries);

        int take = Math.min(RANDOM_POOL_SIZE, entries.size());
        for (int i = 0; i < take; i++)
            moviePool.put(entries.get(i).getValue(), entries.get(i).getKey()); // title → id

        List<String> titles = new ArrayList<>(moviePool.keySet());

        for (int i = 0; i < ROWS; i++) {
            cmbMovies[i].removeAllItems();
            for (String t : titles) cmbMovies[i].addItem(t);
            if (i < titles.size()) cmbMovies[i].setSelectedIndex(i);
            txtRatings[i].setText("");
        }
    }

    

    private void showResults(int X, int K, List<String> recs) {
        StringBuilder sb = new StringBuilder();
        sb.append("X = ").append(X).append("  |  K = ").append(K)
          .append("  |  Total recommendation:").append(recs.size()).append("\n");
        sb.append("─".repeat(55)).append("\n\n");

        if (recs.isEmpty()) {
            sb.append("No recommendation found.");
        } else {
            for (int i = 0; i < recs.size(); i++) {
                if (i % K == 0)
                    sb.append("[ Similar User #").append(i / K + 1)
                      .append(" — Highest ").append(K).append(" Movie ]\n");
                sb.append("  ").append(i % K + 1).append(". ").append(recs.get(i)).append("\n");
                if (i % K == K - 1) sb.append("\n");
            }
        }
        txtResults.setText(sb.toString());
        txtResults.setCaretPosition(0);
    }

